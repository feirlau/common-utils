package org.feirlau.dwr;

public class Person {
	private String name;
	private int age;
	private String desc;
	
	public String getName() {
		return name;
	}
	public void setName(String v) {
		name = v;
	}
	
	public int getAge() {
		return age;
	}
	public void setAge(int v) {
		age = v;
	}
	
	public String getDesc() {
		return desc;
	}
	public void setDesc(String v) {
		desc = v;
	}
}
